// Apply translations
window.applyI18n = function (lang) {
  const dict = window.I18N[lang] || window.I18N.ru;
  document.documentElement.lang = lang === 'kk' ? 'kk' : 'ru';

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    const val = dict[key];
    if (val == null) return;

    const attr = el.getAttribute('data-i18n-attr');
    if (attr) {
      el.setAttribute(attr, val);
      return;
    }
    if (el.hasAttribute('data-i18n-html') || /<\w+/.test(val)) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Special: <title>
  if (dict['meta.title']) document.title = dict['meta.title'];

  // Toggle active button state
  document.querySelectorAll('.lang-btn').forEach((b) => {
    b.classList.toggle('is-active', b.dataset.lang === lang);
  });

  // Re-render lucide icons (innerHTML may have wiped some)
  if (window.lucide) window.lucide.createIcons();

  // Persist
  try { localStorage.setItem('ct_lang', lang); } catch (e) {}
  window.CURRENT_LANG = lang;
};

// Init on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  let saved = null;
  try { saved = localStorage.getItem('ct_lang'); } catch (e) {}
  const lang = saved === 'kk' || saved === 'ru' ? saved : 'ru';
  window.applyI18n(lang);

  document.querySelectorAll('.lang-btn').forEach((btn) => {
    btn.addEventListener('click', () => window.applyI18n(btn.dataset.lang));
  });
});
